    public void fillData() {
        DefaultTableModel model = (DefaultTableModel) tbTable.getModel();
        model.setRowCount(0);
        for (Student st : dao.getAll()) {
            Object[] rowdata = new Object[6];
            rowdata[0] = st.getId();
            rowdata[1] = st.getName();
            rowdata[2] = st.getPoint();
            rowdata[3] = st.getBrach();
            rowdata[4] = st.getStudy();
            rowdata[5] = st.isRole() ? "Yes" : "No";
            model.addRow(rowdata);
        }
    }
