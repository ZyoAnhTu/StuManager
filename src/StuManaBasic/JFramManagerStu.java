    public Student getModel() {
        Student ee = new Student();
        ee.setId(txtID.getText());
        ee.setName(txtUser.getText());
        ee.setPoint(Double.parseDouble(txtPoint.getText()));
        ee.setBrach(cbbBranch.getSelectedItem().toString());
        ee.setStudy(txtStudy.getText());
        return ee;
    }
